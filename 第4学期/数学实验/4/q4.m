t=[0,0.25,0.5,1,1.5,2,3,4,6,8];
c=[30,19.21,18.15,15.36,14.10,12.89,9.32,7.45,5.24,3.01];

plot(t,c);
title('药物浓度随时间的变化规律')

p1=polyfit(c,t,3);
y10=polyval(p1,10);
y25=polyval(p1,25);
disp(['c=10,y= ' num2str(y10)]);
disp(['c=25,y= ' num2str(y25)]);
disp(y10-y25);

